	public void rectangleVide(int x, int y, int z, int longueur, int largeur, Block block)
	{
		for (int i = 0; i < longueur; i++)
		{
			for (int j = 0; j < largeur ; j++)
			{
				if (j == 0 || j == largeur - 1 || i == 0 || i == longueur - 1)
					this.w.setBlock(x + i, y, z + j, Blocks.snow);
			}
		}
	}
	
	public void rectangle(int x, int y, int z, int longueur, int largeur, Block block)
	{
		for (int i = 0; i < longueur; i++)
		{
			for (int j = 0; j < largeur ; j++)
			{
				this.w.setBlock(x + i, y, z + j, block);
			}
		}
	}
	
