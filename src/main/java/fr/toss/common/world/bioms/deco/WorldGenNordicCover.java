	@Override
	public void generate(Random random, int chunkX, int chunkZ, World world, IChunkProvider chunkGenerator, IChunkProvider chunkProvider)
	{
		if (random.nextInt(20) == 0 && this.biomIsValid(world.getBiomeGenForCoords(chunkX, chunkZ)))
		{
			System.out.println("gen");

			int x = chunkX * 16;
			int z = chunkZ * 16;
			int y = world.getTopSolidOrLiquidBlock(x, z) - 3;
			WorldGenNordicCover.spawn(world, x, y, z);
			x -= (4 + world.rand.nextInt(20));
			z -= (4 + world.rand.nextInt(20));
			WorldGenMoulin.spawn(world, x, world.getTopSolidOrLiquidBlock(x, z) - 3, z);
		}
	}

	boolean biomIsValid(BiomeGenBase b)
	{
		return	(b.biomeID == BiomeGenBase.coldTaiga.biomeID) ||
				(b.biomeID == BiomeGenBase.icePlains.biomeID) ||
				(b.biomeID == BiomeGenBase.mesa.biomeID) ||
				(b.biomeID == BiomeGenBase.mesaPlateau.biomeID) ||
				(b.biomeID == BiomeGenBase.mesaPlateau_F.biomeID) ||
				(b.biomeID == BiomeGenBase.plains.biomeID) ||
				(b.biomeID == BiomeGenBase.savannaPlateau.biomeID) ||
				(b.biomeID == BiomeGenBase.savanna.biomeID);
	}
	
